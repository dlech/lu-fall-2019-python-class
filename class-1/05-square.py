'''How to draw a square with turtle graphics'''

import turtle

# The square has 4 sides. Each side of the square is 100 pixels long. Each
# corner has an angle of 90 degrees.
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
